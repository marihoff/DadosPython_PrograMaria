#➡️Elementos Comuns:

#Título: Adicione um título descritivo para o gráfico.

#Legenda: Ative ou desative a legenda e personalize seu estilo (fonte, cor, posição).

#Cores: Selecione cores para as diferentes categorias, séries ou dados do gráfico. Você pode usar cores predefinidas, escolher cores da paleta ou inserir códigos hexadecimais.

#Formatação de Texto: Altere a fonte, tamanho, cor e alinhamento do texto usado em títulos, rótulos e legendas.

#Borda: Adicione uma borda ao redor do gráfico, personalizando sua cor, espessura e estilo.

##Plano de Fundo: Defina uma cor de fundo para o gráfico ou utilize imagens.

#Margem: Ajuste o espaço em branco ao redor do gráfico.

#Elementos Interativos: Habilite ou desative a interatividade do gráfico, permitindo que os usuários explorem os dados clicando ou passando o mouse sobre os elementos.

#➡️Elementos Específicos por Tipo de Gráfico:

#Gráficos de Linha:

#Tipo de Linha: Escolha entre linhas sólidas, pontilhadas, tracejadas ou outras opções.

#Espessura da Linha: Ajuste a espessura das linhas do gráfico.

#Marcadores: Adicione marcadores nas linhas para destacar pontos de dados específicos. Personalize o estilo dos marcadores (formato, cor, tamanho).

#Eixos: Configure a aparência dos eixos X e Y, incluindo rótulos, escala e grade.

#Gráficos de Colunas:

#Largura das Colunas: Ajuste a largura das colunas do gráfico.

##Arredondamento das Colunas: Defina o arredondamento das bordas das colunas.

#Empilhamento de Colunas: Ative ou desative o empilhamento de colunas para comparar valores de diferentes categorias.

#Espaçamento entre Colunas: Ajuste o espaço entre as colunas do gráfico.

#Gráficos de Torta:

#Porcentagem em Rótulos: Exiba a porcentagem de cada segmento da torta ao lado dos rótulos.

#Destaque de Segmentos: Selecione um segmento da torta para destacá-lo visualmente.

#Etiqueta Interna: Adicione etiquetas dentro dos segmentos da torta para mostrar valores específicos.

#Gráficos de Dispersão:

#Tamanho dos Pontos: Ajuste o tamanho dos pontos de dados no gráfico.

#Cor dos Pontos: Personalize a cor dos pontos de acordo com uma variável específica.

#Forma dos Pontos: Escolha entre diferentes formas para os pontos de dados (círculos, quadrados, triângulos, etc.).

#Eixos: Configure a aparência dos eixos X e Y, incluindo rótulos, escala e grade.

#Gráficos de Mapa de Calor:

#Escala de Cores: Escolha uma escala de cores para representar a intensidade dos dados no mapa.

#Limites de Cor: Defina os limites superior e inferior da escala de cores.

#Legendas de Cores: Crie uma legenda para explicar a relação entre cores e valores de dados.

#Marcadores: Adicione marcadores no mapa para destacar pontos de dados específicos.

#Gráficos de Métricas:

#Alinhamento do Texto: Alinhe o texto da métrica à esquerda, direita ou centralizado.

#Formatação Numérica: Personalize a formatação dos números, incluindo casas decimais, separadores de milhar e símbolos de moeda.

#Indicadores de Tendência: Adicione setas para indicar se o valor da métrica está aumentando ou diminuindo em relação ao período anterior.