#Nesta aula começamos a criar o nosso dashboard no looker:

#O Looker Studio permite conectar-se a diversas fontes de dados, como bancos de dados, APIs e arquivos em nuvem. A plataforma oferece uma ampla gama de ferramentas para criar visualizações personalizadas, como gráficos, tabelas, mapas e painéis dinâmicos.🚀

#Vamos lá! Para acesso o Looker:

#Acesse o Looker Studio em um navegador web.

#Clique em "Criar" e selecione "Relatório".

#➡️Conectando os Dados:

#Na nova janela, escolha a opção "Adicionar dados ao relatório".

#Selecione "Planilhas Google" como fonte de dados.

#Informe a URL do arquivo armazenado no Google Drive.

#Clique em "Abrir com" e escolha "Planilhas Google".

#Copie a URL da nova aba do navegador com os dados no Google Planilhas.

#Cole a URL no Looker Studio e clique em "Adicionar".

#➡️Explorando a Interface:

#Lado Esquerdo: Área de construção do dashboard, onde os elementos (informações e gráficos) serão inseridos.

#Lado Direito:

#Gráfico: Contém opções de estilo e configuração para os gráficos criados.

#Dados: Exibe as colunas da tabela de dados e permite adicionar campos, parâmetros e novos dados.

