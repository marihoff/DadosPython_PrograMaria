#➡️Como criar os Elementos:

#Criar um gráfico

#Para criar um gráfico clique em "Adicionar um gráfico" no canto superior esquerdo e escolha o tipo de gráfico que quer adicionar: Visão Geral, Pizza, Colunas Empilhadas, Barras Empilhadas, etc… Não se preocupe, você pode alterar o seu tipo de gráfico depois que criar caso não goste do resultado. 

#Selecione o tipo desejado na lista de opções, arraste e solte o gráfico na página.

#Criar Filtros:

#Para cada indicador que precisa de um filtro, clique em "Adicionar um filtro" na aba de configurações do gráfico.

#Na tela de criação de filtro:

#Nome do Filtro: Digite um nome descritivo (por exemplo, "Genero_feminino", "não_brancas", etc.).

#Tipo de Filtro: Selecione "Incluir" ou "Excluir" (conforme a necessidade).

#Coluna: Selecione a coluna que contém os dados para o filtro (por exemplo, "Gênero", "Cor-raça-etnia", etc.).

#Critério: Defina o critério de filtragem (por exemplo, "Igual a", "Contém", etc.).

#Valor: Digite o valor a ser filtrado (por exemplo, "Feminino", "Branca", etc.).

#Clique em "Salvar".

#Posicionar Elementos:

#Clique e arraste os gráficos para a posição desejada na página.

#Utilize as guias azuis para alinhar os elementos.

#Alterar Estilo:

#Na aba de configurações do gráfico, clique em "Estilo".

